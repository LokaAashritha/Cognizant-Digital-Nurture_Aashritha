public class Exercise06_DataTypeDemonstration {

    public static void main(String[] args) {

        int employeeIdValue = 101;

        float productPriceValue = 499.99f;

        double accountBalanceValue = 125000.75;

        char gradeLetterValue = 'A';

        boolean isProjectCompletedValue = true;

        System.out.println("Integer Value: " + employeeIdValue);
        System.out.println("Float Value: " + productPriceValue);
        System.out.println("Double Value: " + accountBalanceValue);
        System.out.println("Character Value: " + gradeLetterValue);
        System.out.println("Boolean Value: " + isProjectCompletedValue);
    }
}